export function parseGameRangerList(raw: string): string[] {
  const result: string[] = [];
  const dedupe = new Set<string>();
  const lines = raw.split(/\r?\n/);
  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    // skip single-letter index headings
    if (/^[A-Z]$/.test(trimmed)) continue;
    // split by tabs (columns)
    const tokens = trimmed.split(/\t+/).map((t) => t.trim()).filter(Boolean);
    for (let token of tokens) {
      // strip stray attachment markers
      token = token.replace(/\(See .*$/i, '').trim();
      if (!token) continue;
      const key = token.toLowerCase();
      if (!dedupe.has(key)) {
        dedupe.add(key);
        result.push(token);
      }
    }
  }
  return result;
}

